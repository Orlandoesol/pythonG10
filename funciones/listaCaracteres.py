def usar_metodos_lista():
    lista = ["manzana", "banana", "cereza", "durazno", "uva"]

    print("Lista original:", lista)

    print("Método append:")
    lista.append("fresa")
    print("Lista después de append:", lista)
    
    print("Método extend:")
    lista.extend(["mango", "naranja", "pera"])
    print("Lista después de extend:", lista)

usar_metodos_lista()

